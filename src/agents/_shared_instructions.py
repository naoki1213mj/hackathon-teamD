"""Phase 12: 全エージェント共通の instructions フラグメント。

Gap 5: パイプライン全体観の DRY 化 — 8 ステップを 1 箇所で定義し、
       各エージェントの INSTRUCTIONS 冒頭に注入する。
Gap 1: 追加提案・追加質問禁止ルールの統一文言定義。
Gap 6: ユーザー要求スコープ尊重ルールの定義。
Gap 7: Evidence reporting ルールの参照定数。
"""

# 8 ステップのパイプライン全体像（完全版）
_PIPELINE_STEPS = (
    "## パイプライン全体の流れ\n"
    "1. **データ分析** (Agent1): Fabric Lakehouse から売上・レビューを取得・分析\n"
    "2. **施策立案** (Agent2): 分析結果をもとにマーケティング企画書を作成\n"
    "3. **承認ステップ**: ユーザーが企画書を確認・承認/修正\n"
    "4. **規制チェック** (Agent3a): 承認済み企画書の法令・規制適合性を検証\n"
    "5. **企画書修正** (Agent3b): 規制チェック結果を反映した修正版企画書を生成\n"
    "6. **販促物生成** (Agent4): HTML ブローシャ・ヒーロー画像・SNS バナーを生成\n"
    "7. **動画生成** (Agent5): Photo Avatar で販促動画を生成\n"
    "8. **品質レビュー** (Agent6): 全成果物の最終品質チェック（バックグラウンド・オプション）"
)


def get_pipeline_header(agent_role: str) -> str:
    """各エージェントの INSTRUCTIONS 冒頭に注入するパイプライン全体観を返す。

    Gap 5 実装: INSTRUCTIONS の冒頭「あなたは〜です。## パイプライン全体の流れ」部分を
    1 箇所に集約し、各エージェントが agent_role を渡して注入する。

    Args:
        agent_role: このエージェントの役割タイトル（例: "**データ分析エージェント**"）

    Returns:
        パイプライン冒頭文字列（末尾に改行あり。後続セクションを `+ "\\n## ..."` で連結）
    """
    return f"あなたは旅行マーケティング AI パイプラインの {agent_role} です。\n\n{_PIPELINE_STEPS}\n\n"


# Gap 1: 追加提案・追加質問禁止ルール（統一文言）
# 「出力の注意事項」section に記述するテキストの正規版
# 各エージェントはこの文言を INSTRUCTIONS に直接埋め込む（参照用定数として定義）
NO_FOLLOWUP_RULE = (
    "- 出力末尾に「他にご質問はありますか？」「必要であれば〜できます」"
    "「さらに〜できます」「次に〜可能です」等の追加提案・追加質問は**絶対に書かない**こと。"
    "出力 contract で定められた section だけを書いて終了する。"
)

# Gap 6: ユーザー要求スコープ尊重ルール
# P10 smoke で観測された scope deviation hallucination の根本対策:
# 「年別売上トレンド」prompt に対して agent が「秋・京都・シニア向け」にフィルタしてしまう問題
# ContextVar に元プロンプトが設定されていればそれを参照し、設定されていなければ直前の入力を信用する
SCOPE_RESPECT_RULE = (
    "- **元のユーザー要求のスコープを厳守する**: "
    "勝手に scope を絞り込まない（例: 「年別トレンド」要求を特定の地域・季節だけに限定しない）。"
    "勝手に scope を拡張しない（例: 「ハワイ」プランに他の旅行先の名所を混ぜない）。"
    "ContextVar に元プロンプトが設定されていればそれを参照し、設定されていなければ直前の入力を信用する。"
)
